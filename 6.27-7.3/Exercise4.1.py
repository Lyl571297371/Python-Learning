#练习for循环
numbers=['9','6','2','4']
for number in numbers:#for语句之后是冒号，且下行要缩进，
	print(number+", "+"is my favorite number"+"!")
	print("This is my lucky number "+number+"!\n")
print("I love these numbers"+" !")#5、6行缩进的都是循环的一部分，此行不缩进只执行一次
#注意：for之后的冒号，是为了告诉python下一行是循环的第一行，不能遗漏
#缩进错误很容易导致结果不是自己想要的，这时就要返回看一下自己的缩进
 
 
